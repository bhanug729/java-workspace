package $03_Operators_If_else_NumberSystem;

public class $2_Arithmetic {
	public static void main(String[] args) {
		int a = 8;
		int b = 5;

		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		System.out.println();

		double x = 5;
		double y = 3;
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
	}
}
